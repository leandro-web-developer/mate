<p class="text-justify">
    Uruguay, oficialmente República Oriental del Uruguay, es un país de América del Sur, situado en la parte oriental
    del Cono Sur. Limita al noreste con Brasil —estado de Río Grande del Sur—, al oeste y suroeste con Argentina
    —provincias de Corrientes, Entre Ríos y Buenos Aires, y la Ciudad Autónoma de Buenos Aires (separada por el Río de
    la Plata)— y tiene costas en el océano Atlántico por el sur. Abarca 176 215 km² y es el segundo país más pequeño de
    Sudamérica, después de Surinam.​ Según los datos del último censo del INE en 2011, la población de Uruguay es de 3
    286 314 habitantes,​ con lo que figura en la décima posición entre los 12 países sudamericanos.<br><br>

    Es una república presidencialista subdividida en diecinueve departamentos y 125 municipios. La capital y ciudad más
    poblada del país es Montevideo, con 1,3 millones de habitantes,​ y cuya área metropolitana ronda los 2 millones,
    lo que representa el 56,3 % del total nacional.<br><br>

    Es miembro fundador de las Naciones Unidas,​ del Mercosur, de la OEA y del G77, y forma parte de otros
    organismos internacionales.<br><br>

    El actual territorio uruguayo fue conocido durante la época colonial como Banda Oriental, e incluía parte del actual
    estado brasileño de Río Grande del Sur. El 27 de agosto de 1828 se firmó la Convención Preliminar de Paz, en la
    que se estableció la creación de un estado independiente, aunque sin un nombre oficial. El nombre del nuevo estado
    dado en su primera Constitución fue «Estado Oriental del Uruguay», cambiándose el mismo por el actual en la
    Reforma Constitucional de 1918.<br><br>

    Tiene clima templado con una temperatura media de 17,5 ºC, siendo enero el mes más cálido, con una media de 22,6 ºC,
    y julio el mes más frío, con una media de 10,6 ºC. Las lluvias son abundantes y varían de los casi 1000 mm por
    año en el sur a los 1500 mm en el norte, en la frontera con Brasil. Las precipitaciones tienen también
    variaciones estacionales, siendo los meses de otoño y primavera los que registran precipitaciones más abundantes.<br><br>

    Los principales recursos económicos son la agricultura, la forestación y la ganadería. Los recursos minerales y
    energéticos son escasos, y las principales industrias son las del papel y el cartón, el cemento y la refinería de
    petróleo.<br><br>

    Según las Naciones Unidas, es el país de América Latina con el nivel de alfabetización más alto. De acuerdo al
    estudio de la organización Transparencia Internacional, es el país de Latinoamérica con menor Índice de Percepción
    de Corrupción y el 23° en el mundo. El Programa de las Naciones Unidas para el Desarrollo (PNUD) dice que es el
    tercer país de Latinoamérica (después de Chile y de Argentina) con mayor Índice de Desarrollo Humano (IDH) y el 54º
    en el mundo. De acuerdo a la Comisión Económica para América Latina y el Caribe (Cepal), es uno de los países
    de la región con una distribución de ingresos más equitativa, con un Coeficiente de Gini de 0,39. También es el
    cuarto país de Latinoamérica (después de Cuba, Costa Rica y Chile) con la esperanza de vida más alta. En el
    año 2018 es el tercer país de Latinoamérica (luego de Panamá y de Chile) con el PIB (PPA) per cápita más alto.<br><br>

    La Corporación Latinobarómetro, en un estudio realizado en 2008, lo sitúa como el país más pacífico de América
    Latina. Además, según la revista estadounidense International Living es el mejor de Latinoamérica para
    vivir. Esta misma publicación asegura que se encuentra entre los veinte países más seguros del mundo,31​ mientras
    que la publicación británica The Economist, lo ubica entre los veinte más democráticos, siendo el único país
    sudamericano considerado por dicho índice como una «democracia plena».
