<h3>Uruguay</h3>
<p class="text-justify">
    El mate es considerado en Uruguay como bebida nacional, se trata del país con el mayor consumo per cápita de yerba
    mate a nivel mundial, con un consumo por persona de 8 kilogramos por año. El consumo del mate es una de las
    costumbres más extendidas por todo el territorio del país, tanto en zonas rurales como urbanas, así como también a
    través de todas las clases sociales.<br><br>

    El mate uruguayo se toma caliente y amargo, y se compone de un set de piezas: el agua se porta en un termo que
    mantiene su calor, la yerba mate se deposita en un recipiente también denominado mate que se obtiene del porongo (la
    planta Lagenaria siceraria); y el agua es absorbida mediante una bombilla.<br><br>

    Uno de los aspectos más característicos de su consumo en Uruguay es el ritual del mate y su función social; el mate
    generalmente se consume en rueda, es decir en grupo, con un cebador que es el encargado de prepararlo y añadir el
    agua para que el resto de los integrantes lo consuman por turno y en sentido horario, esta se trata de una antigua
    tradición de origen indígena o campestre, pero que se ha mantenido intacta hasta el día de hoy.<br><br>

    Su importante función cohesiva en la sociedad uruguaya fue descrita de la siguiente manera por el antropólogo
    uruguayo Daniel Vidart
</p>

<blockquote class="blockquote text-right">
    <p class="mb-0">
        "El mate, a su modo, empareja las clases sociales, porque a todos pertenece. Lo bebieron patrones y peones en la
        rueda patriarcal de otrora; jefes y soldados en los vivacs revolucionarios; amos y esclavos en los floridos
        patios de las casonas coloniales. Y en todos los tiempos fue el mate el que hizo la rueda y no la rueda la que
        trajo al mate."
    </p>
    <footer class="blockquote-footer"><cite title="Daniel Vidart">Daniel Vidart</cite></footer>
</blockquote>
<p>
    Desde 1920 Uruguay importa prácticamente el 100 % de la yerba mate que consume a nivel comercial, sin embargo la
    planta Ilex Paraguariensis se desarrolla naturalmente en los bosques serranos del Uruguay; actualmente existen
    proyectos en desarrollo para la producción comercial a pequeña escala de la yerba mate en este de Uruguay.<br><br>

    A partir de 1978 funciona en el centro de Montevideo el museo del Gaucho y la Moneda, que cuenta con la mayor
    exhibición de mates y bombillas del país, con ejemplares que datan desde la época del virreinato a nuestros días,
    algunos de los cuales pertenecieron a personajes importantes de la historia uruguaya o se destacan por su trabajo
    artístico o decorativo.
</p>

<h3>Propiedades</h3>
<p class="text-justify">
    Su capacidad diurética explica una paradoja de la dieta gaucha hasta inicios del siglo xx: hasta entonces los
    habitantes de las zonas rurales del Cono Sur solían tener una dieta hiperproteínica sin aparente contrabalanceo, lo
    cual hubiera provocado ―en el más benigno de los casos― una elevadísima concentración de ácido úrico, que se
    reflejaría rápidamente en afecciones imposibilitantes como la gota. Sin embargo, la diuresis que se lograba por la
    elevada ingesta de mate contrapesaba los excesos de una dieta hiperproteica.[cita requerida] El mate, por otra
    parte, posee excelentes antioxidantes. Según las conclusiones de un estudio llevado a cabo en Estados Unidos, el
    consumo de mate puede reducir el colesterol LDL o colesterol «malo», a la vez que promueve el aumento de colesterol
    HDL o colesterol «bueno». La investigación afirma que tiene propiedades que inducen la actividad de importantes
    enzimas antioxidantes en el organismo; una de las más importantes es la paraoxonasa-1, que ayuda a retirar el
    colesterol malo y tiene efectos cardioprotectores; del mismo modo el mate ayuda a limpiar los intestinos al
    favorecer la motilidad de los mismos.También aporta el mate (especialmente si es bebido mediante el «cebado» con
    bombilla) elevados niveles de xantinas y hasta doce beneficiosos polifenoles entre los cuales se destacan el ácido
    clorogénico y la quercitina​ Sus condiciones antioxidantes son similares a las del té verde, aunque el mate es más
    nutritivo ya que aporta vitaminas B1, B2, B3, B5, C y E, así como hierro, magnesio, potasio y más de diez
    aminoácidos.<br><br>

    Otra característica (actualmente casi anecdótica) fue bastante fortuita: los combatientes criollos o gauchos durante
    la Guerra de la Independencia obtenían la mayor parte de su agua a través de la ingesta de infusiones y decoctos de
    mate, de este modo el agua era purificada de gran parte de las bacterias y posibles parásitos; los europeos
    (españoles y mercenarios) que solían considerar al mate como algo «bárbaro» o «primitivo» bebían las aguas sin el
    necesario caldeado, por lo cual contraían parasitosis con frecuencia.<br><br>

    No es adictivo, y no se han registrado casos de insomnio debidos a este;​ lo cual se debe a que el mate se bebe
    bien diluido en agua y ―generalmente― entre varias personas, y que además de todo esto, el mate contiene mucha menos
    cafeína que el té, el café, el guaraná o el cacao. También podría influir que este es consumido preferentemente
    en la franja horaria que va desde la mañana temprano hasta la tarde (hasta las 21:00, aproximadamente). Por otra
    parte la diuresis que provoca el mate elimina rápidamente los metabolitos de alcaloides de la cafeína.
</p>

<h3>Asociación con enfermedades</h3>
<p class="text-justify">
    Un estudio previo de Uruguay informó de un aumento del riesgo de cáncer de vejiga asociados al beber de la yerba
    mate de manera excesiva. También se sugiere que el consumo de mate pueda llegar a ser un factor de riesgo para el
    cáncer de esófago, «si se bebe a punto de ebullición (100 °C o a una temperatura muy caliente, tal problema ocurre
    con todos los alimentos si son consumidos muy calientes), debido a la alta temperatura del agua» con que suele ser
    bebido lo cual —con el paso del tiempo— puede llegar a dañar la mucosa esofágica.<br><br>

    Aunque es obvio que si se comen o beben alimentos a elevada temperatura de un modo crónico, las elevadas
    temperaturas de ingesta pueden provocar enfermedades de todo tipo; como por ejemplo cánceres de esófago, estómago,
    lengua, labios y de todo el tracto digestivo superior. Así ha sido frecuente en la cultura japonesa en la cual los
    varones bebían por prestigio el té verde sin ser lo suficientemente enfriado o entibiado unos minutos antes que lo
    bebieran las mujeres y este verdadero ritual provocaba epidemiológicamente una muy elevada incidencia de cáncer de
    estómago o de esófago entre los varones japoneses que bebían la infusión del té a temperaturas nocivamente elevadas.
    No obstante, pueblos tan «materos» como el argentino, paraguayo y uruguayo están libres de estadísticas que en
    verdad sugieran una presencia destacada de enfermedades causadas por beber mate.<br><br>

    Párrafo aparte en cuanto a la posible toxicidad merece el tratamiento masivamente industrial de las hojas de mate;
    tradicionalmente las mismas son cancheadas es decir secadas al sol sin ningún añadido químico artificial, pero las
    industrias comerciales en muchas ocasiones aceleran artificialmente la desecación de las hojas de un modo nocivo: Un
    grupo de investigadores del National Cancer Institute (de Estados Unidos) y de la Universidad Federal de Santa María
    (de Brasil), hallaron que las hojas secas procesadas y las infusiones de mate realizadas con ocho marcas comerciales
    diferentes contenían muy altos niveles (de 8.03 a 53.3 ng/g de hojas secas) del carcinógeno hidrocarburo aromático
    policíclico (HAP o PAH) (por ejemplo benzopireno). Estos compuestos potencialmente carcinógenos se originan por el
    proceso comercial de secado de las hojas de yerba mate, el cual involucra el uso de humo del quemado de madera,
    análogamente al caso de los hidrocarburos aromáticos policíclicos encontrados en la carne ahumada. El estudio
    concluye que en el mate se encontró una «muy alta concentración de hidrocarburos aromáticos policíclicos
    carcinógenos en las hojas de yerba mate y en las infusiones frías y calientes». en todo caso -como ocurre con
    casi todo en la vida- deben evitarse los excesos: el mate es una infusión cuyo consumo es muy benéfico que solo
    presenta riesgos si se bebe de un modo excesivo o con frecuencia a temperaturas mayores a los 65 °C.
</p>

<h3>Preparación</h3>
<p class="text-justify">
    Para preparar un mate cebado, se coloca la yerba en un recipiente llamado mate o calabaza, hasta las tres cuartas
    partes del mismo. Luego se tapa con la mano, se coloca boca abajo y se lo agita (esto hace que las partículas más
    finas queden en la parte superior, y no obstruyan la bombilla). Se lo coloca nuevamente boca arriba y se le agrega
    un poco de agua tibia o fría cerca del borde. Se deja reposar algunos segundos (hasta que se absorba el agua) y se
    termina de llenar con agua caliente, hasta aproximadamente 7 u 8 mm del borde, cuidando de que no se moje la yerba
    de la superficie (el agua caliente debe estar a una temperatura cercana a 80 °C, antes del punto de ebullición).
    Luego de uno o dos minutos se ensilla, es decir, se coloca la bombilla tapándole la boca con el dedo pulgar y
    presionando firmemente hasta el fondo. Siempre tiene que quedar más yerba sobre el lado opuesto de la bombilla. Se
    debe tener cuidado de no remover demasiado la yerba, porque podría taparse la bombilla. La bombilla se debe inclinar
    en sentido contrario a donde quedó la yerba seca, es decir, para el lado del que va a tomar el mate.<br><br>

    Es importante destacar la temperatura que debe tener el agua a la hora de cebar el mate. En varias provincias de
    Argentina la temperatura del agua debe ser entre 70 y 90 ℃, es decir antes de que rompa el hervor. En las provincias
    del Noreste argentino, en el estado de Mato Grosso del Sur en Brasil, así como en Paraguay, al mate frío se lo llama
    «tereré» ya que se ceba con agua fría o jugo natural de naranjas por ejemplo.<br><br>

    El tomar mate, se ha convertido en un hábito social que se realiza muchas veces en conjunto. Es decir que varias
    personas comparten el mismo mate, llenándolo completamente para cada bebedor, donde uno de ellos oficia de
    «cebador». Este cebador es el encargado de llenar el mate y, a modo de ronda, pasarlo al siguiente bebedor.<br><br>

    Pava eléctrica y mate de madera.<br>
    También es un hábito muy común endulzarlo. Esto puede hacerse de dos maneras: una de ellas es mezclar el endulzante
    con el agua, con lo cual se logra un sabor homogéneo; y la otra es agregar el endulzante entre cebada y cebada.
</p>

<h2>Recipientes</h2>
<h3>Historia</h3>
<p class="text-justify">
    La infusión de la yerba mate tomada con bombilla se conoce como «mate». Los guaraníes, quienes fueron los primeros
    en incluir esta infusión en su gastronomía llamaban a la infusión ka'aý, significando: ka'á= «yerba mate» e y=
    «agua». Así es que ka'aý significaría «agua con yerba mate». A su vez, el recipiente usado para tomar mate era
    llamado en guaraní ka'ayguá; guá = «recipiente»; entonces una traducción literal de ka'ayguá sería «recipiente (para
    tomar) agua de yerba mate».<br><br>

    El motivo por el cual se le dice «mate» a esta infusión hay que buscarlo en una influencia lingüística antigua,
    similar a las que existen hoy en día. Así es como, revisando en la Historia, hallamos que el Imperio Inca influía en
    todos los otros pueblos de la región tanto cultural como lingüísticamente, entre los pueblos influenciados por los
    incas estaban los guaraníes. La lengua oficial del Imperio Inca era el quichua o quechua, y en la variedad sureña
    del quichua el vocablo mati es como se ha llamado desde aquella época al fruto de la planta Lagenaria, nativa de
    Sudamérica.<br><br>

    La Lagenaria Vulgaris y la Lagenaria Siceraria son tal vez las más conocida de las lagenarias de Sudamérica. Los
    frutos de las lagenarias se han usado como: alimento, elemento para transportar agua o legumbres y para tomar mate
    con bombilla, aún desde tiempo precoloniales.<br><br>

    Las plantas en cuestión, las lagenarias, según la subvariedad da distintos frutos que difieren en tamaño y forma.
    Hay frutos grandes y otros chicos, hay frutos que tienen una estrangulación y otros que presentan una forma más
    redondeada. Los pueblos pertenecientes al Imperio Inca, entre ellos los pueblos que habitaban lo que actualmente es
    el Noroeste argentino, distinguían varios tipos de mati o mate. A los mati pequeños y de forma redondeada o forma
    que se parece a una pera le decían «puru». A los mati más grandes, que presentan una estrangulación natural y a un
    lado una sección de forma más bien redonda y al otro lado otra sección más grande y de forma más bien ovalada le
    decían «purungu». Esos términos ("mati", "puru" y "purungu") penetraron en la sociedad guaraní, en la de las
    Misiones Jesuíticas-Guaraníes, y en general en toda la sociedad virreinal de gran parte del cono sur americano.
</p>

<h3>Tipos de mate</h3>
<p class="text-justify">
    De esta forma es como hoy en día han llegado a nosotros las distintas formas de llamar a los frutos de la Lagenaria
    Siceraria. Dichos frutos se utilizan hoy en día principalmente para tomar la infusión de Ilex Paraguariensis, más
    conocida con su nombre vernáculo «yerba mate». Por tanto, los términos «mate», «poro» y «porongo» son palabras del
    quichua que han llegado a nuestros días. Cada región del cono sur tiene su forma de emplear estas palabras a la hora
    de referirse a los distintos mates (recipientes para tomar mate) hechos de Lagenaria. Sin embargo, hay nombres que
    técnicamente son correctos y otros que no lo son. A continuación se detallan los nombres técnicamente correctos para
    denominar a cada recipiente de mate, frutos de las lagenarias, según la forma que tiene el fruto denominado «mate» y
    según el corte que se le hace.
</p>

<ul>
    <li>
        <p>
            Poro: palabra que proviene del quichua puru. Se usa para referirse a un mate de pequeño a mediano, que tiene
            forma parecida al de una pera aunque algo más redonda. También se le llama «mate pera» por su forma
            comparada al de una pera (fruta). Están los "falsos poros" que son en realidad lo que queda al hacer el
            corte para hacer un mate del tipo porongo, a veces cortado un poco más arriba de lo normal.
        </p>
    </li>
    <li>
        <p>
            Porongo': palabra que deriva del quichua purungu. Los guaraníes en su propio idioma llamaban yeruá (jeru'a,
            en grafía guaraní) a lo que en quichua se llamaba purungu. Esta palabra de raíz quechua se utiliza para
            denominar a un mate más grande que el de tipo "poro". Tiene una base más angosta que la boca, que es ancha
            siempre, y justo antes de la boca tiene una estrangulación natural. También a veces se le llama "mate de
            camionero" (por el hecho de que los camioneros suelen usarlo para sus largos viajes porque al caberle más
            cantidad de yerba la cebada del mate dura más), "mate bocón" o "mate de boca ancha".
        </p>
    </li>
</ul>

<p>
    «Mate» es la palabra que deriva del quichua mati. Se utiliza para denominar a todos los frutos de las plantas del
    tipo Lagenaria, sin importar su tamaño o forma. Incluye a los poros, porongos, galleta, etc. Por extensión y con el
    paso del tiempo, esta palabra también se ha usado para llamar a la infusión de yerba mate que se toma con bombilla
    en un recipiente de mate, propiamente dicho, o en cualquier recipiente hecho de algún otro material como madera
    nativa o vidrio.<br><br>

    En la actualidad y según la región de América del Sur, las palabras «mate», «porongo» y «poro» se usan de distintas
    formas y para referirse a distintos objetos. En el Cono Sur todas estas palabras se asocian a la infusión de yerba
    mate, el mate. Aunque algunos se animan a llamar al mate propiamente dicho (al fruto) "mate de calabaza" o
    "calabaza" el nombre original, más apropiado y técnicamente correcto del mate (el recipiente) es "mate" o "mati".
</p>

<h3>Otros tipos de mates tradicionales</h3>
<p class="text-justify">
    Por extensión, en la actualidad el vocablo «mate» se aplica a cualquier recipiente que ha sido hecho para tomar mate
    con bombilla, no importando si está cebado en uno del tipo poro, en uno del tipo porongo, en un pico de porongo, en
    uno del tipo galleta (esos mates cuya forma parecen una 'galleta de campo', un tipo de pan algo achatada, y
    normalmente usados para tomar mate amargo), en un mate de madera nativa, en un mate hecho de guampa de toro, o en
    alguno hecho de otros materiales más modernos y menos acorde a la tradición.<br><br>

    Así es que entre los otros mates tradicionales encontramos los distintos tipos de mate galleta, y mates hechos de
    madera nativa.<br><br>

    Existen diversos tipos de «mates» recipientes para preparar la infusión. El más tradicional, utilizado ya por los
    indígenas guaraníes, y que mantiene su vigencia en la actualidad, es el «mate de calabaza» o «mate de porongo»
    fabricado ahuecando el fruto de dicha cucurbitácea. Debido a la irregularidad de los frutos, suelen poseer elementos
    agregados para su estabilidad y mejor apoyo, evitando el volcado del contenido: algunas versiones poseen un aro de
    base y patitas metálicas y desmontables, mientras otras están revestidas en cuero y las patas de apoyo están hechas
    en este mismo material. Otros agregados más sencillos para estabilizar el recipiente suelen ser bases realizadas en
    cuero tejido, pero independientes del mate. Muchos de los cuencos hechos en calabaza poseen asimismo un
    revestimiento metálico en la boca, para el apoyo de la bombilla y para evitar que el borde del recipiente se vaya
    dañando con la humedad y el uso. Tanto el fruto o porongo como las piezas metálicas de protección pueden estar
    talladas con motivos florales, tradicionalistas o rurales, de manera artesanal. Versiones más sofisticadas poseen
    elaborados pies realizados en metal (plata, alpaca, etc.) o incluso tapas en este mismo material. Los mates de
    calabaza revestidos en cuero también pueden estar trabajados con guardas, coloreados o tratados para dar
    textura.<br><br>

    Una versión rudimentaria es el «mate de caña» o «mate de tacuara» realizado con piezas de caña de azúcar cortadas y
    pulidas, aprovechando las secciones naturales de la caña como base del recipiente. Si bien pueden poseer tallados
    decorativos, suelen ser más sencillos. También en ámbitos rurales especializados en la ganadería, son
    característicos los «mates de asta» (también conocido como «guampa») y los «mates de pezuña», fabricados con dichas
    partes del vacuno, ahuecadas y tratadas aprovechando las cualidades de los huesos del animal.<br><br>

    Además del recipiente de calabaza, otra variante mayoritaria es el «mate de madera», trabajado generalmente en palo
    santo, algarrobo, ñandubay, o quebracho; se recomienda usar maderas duras y estacionadas, y materiales resistentes,
    tratados para que no sufran la acción de las termitas o de otros insectos, que pueden estar ocultos en la pieza
    trabajada, y que se hacen visibles tardíamente, cuando atraviesan las paredes del recipiente. Esta variante del mate
    se presenta tanto con la madera dejada a la vista, en crudo o pulida y pintada, como en versiones revestidas en
    metal o en cuero. Algunos artesanos aprovechan variedades más exóticas de maderas para trabajar piezas de lujo.
</p>

