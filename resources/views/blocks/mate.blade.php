<h1 id="nota-mate">Mate (infusión) </h1>
<p>[ Podés leer este <a href="https://es.wikipedia.org/wiki/Mate_(infusión)" target="_blank">artículo completo en
        Wikipedia</a> ] con todas las referencias, imágenes y mucho más</p>

<p class="text-justify">
    El mate es una infusión hecha con hojas de yerba mate (Ilex paraguariensis), planta originaria de las cuencas de los
    ríos Paraná, Paraguay y Uruguay. Estas plantas previamente secadas, cortadas y molidas forman la yerba mate, la cual
    tiene sabor amargo debido a los taninos de sus hojas. Por esto, hay quienes gustan de endulzar un poco el mate con
    azúcar, stevia o endulzante no calórico, pero comúnmente se toma amargo. La espuma que se genera al «cebar» se debe
    a los glucósidos que la yerba mate contiene.<br><br>

    Es consumido en América desde la época prehispánica por algunas etnias de origen tupí-guaraní, como los avá, los
    mbyá y los kaiowa, y también, en menor medida, por otras etnias que realizaban comercio con ellos, como los ñandevá,
    los taluhets (pampas antiguos) y los qom (tobas).<br><br>

    Fue adoptado rápidamente por los colonizadores españoles y quedó como parte del acervo cultural principalmente en
    Argentina, Paraguay, Uruguay, Sur de Brasil y Mato Grosso del Sur, así como sur y oriente de Bolivia y
    Chile, especialmente en el centro y sur del país (entre Santiago y Magallanes). A su vez, es consumido normalmente
    en Siria (el mayor importador de yerba mate en el mundo) y Líbano, países donde fue llevado por gente que emigró a
    Argentina y retornó.<br><br>

    Como sucede con el té, el café o el chocolate, posee un efecto estimulante debido a la cafeína que contiene.
    Anteriormente, y en forma popular, se pensaba que tenía un energizante similar pero no igual, al que se llamaba
    «mateína».​<br><br>

    Además se le suma un efecto diurético, que es compensado por el alto consumo de agua que se realiza cuando se
    «matea», resultando así una infusión digestiva, depuradora y ―al poseer antioxidantes― preservadora del organismo.
    Como las otras infusiones mencionadas, el mate tiene cierta acidez, razón por la que muchas veces se le añaden ―en
    escasas proporciones― otras hierbas (digestivas, reguladoras de la función hepática, sedantes, etc.) que logran
    neutralizar la acidez como también compensar el ligero efecto estimulante de la cafeína.<br><br>

    Tradicionalmente, se bebe caliente mediante un sorbete denominado bombilla colocado en un pequeño recipiente, que es
    denominado ―según la zona― «mate», «cuia», «porongo» o «guampa», que contiene la infusión.<br><br>

    Mate amargo cebado con montañita de yerba seca y agua caliente a aproximadamente 80 °C.<br><br>

    Mate sin montañita de yerba seca recién cebado.<br>
    Por lo común se distingue al recipiente llamado porongo del llamado «mate» por ser el primero de mayores dimensiones
    y de boca ancha. Aunque se obtienen normalmente del porongo (Lagenaria siceraria), una cucurbitácea cuyo fruto tiene
    una corteza fuerte y leñosa apta para ser usada como recipiente, desde tiempos virreinales se han realizado mates de
    plata, cuerno vacuno (guampa), porcelana, vidrio o madera (en general quebracho o palo santo) o pezuña de toro
    labrada.
</p>

<h3>Nombre científico</h3>
<p class="text-justify">
    El nombre científico es ilex paraguariensis. Ilex porque es un género con unas 400 especies aceptadas, y
    paraguariensis debido a la entonces llamada Provincia Paraguaria.​ Es decir, se denominó así a la yerba mate debido
    a la región donde naturalmente crecía y se consumía esta infusión, esto era en la zona de las Misiones jesuíticas
    guaraníes, las cuales se ubicaban en la antigua Provincia Paraguaria. Esta región o provincia, entonces dependiente
    del Virreinato del Perú, abarcaba regiones de los actuales: Paraguay, Argentina, Uruguay y partes de Bolivia, Brasil
    y Chile
</p>

<h3>Etimología</h3>
<p class="text-justify">
    Al llegar los ibéricos a Sudamérica, notaron que algunas etnias practicaban el ritual de juntarse a beber una
    infusión que los avá llamaban kaay, siendo ka’á: ‘hierba’, e y: ‘agua’. <br>

    La expresión «mate», nace del vocablo quechua matí, que significa calabaza (el recipiente para beber mate suele ser
    hecho de calabaza). El mismo se tomaba a través de una cañita denominada «tacuarí», en cuyo extremo se colocaba una
    semilla ahuecada que hacía las veces de filtro. <br>

    También se lo ha llamado «té del Paraguay» u «oro verde» por el alto valor que tuvo sobre todo en tiempos
    virreinales.<br>

    Por extensión, los conquistadores denominaron de esta manera a la infusión elaborada a partir de la yerba (Ílex
    paraguariensis).<br>

    En portugués se lo llama chimarrão (‘cimarrón’, salvaje), aunque especialmente en el sur de Brasil también se
    utiliza la palabra «mate».
</p>

<h3>Historia</h3>
<p class="text-justify">
    Los antiguos pueblos guaraníes fueron los pioneros en consumir de diversas formas las hojas de yerba mate,
    bebiéndola con agua e incluso mascándolas. Los mismos jesuitas elogiaron los efectos de la yerba, ya que daba un
    cierto vigor al que ingería la infusión, y calmaba la sed mejor que el agua pura. El padre Pedro de Montenegro
    (1663-1728), naturalista, declaró:
</p>
<blockquote class="blockquote">
    <p class="mb-0">
        “Socorrió Dios con esta medicina a esta pobre tierra por ser más conducente a ella que el chocolate, y vino a
        sus naturales habitadores así como lo es el cacao en el Oriente, porque estas tierras muy calientes y húmedas
        causan graves relajaciones de miembros, por la grave aspersión de los poros, y vemos que de ordinario se suda
        con exceso, y no es remedio el vino ni cosas cálidas para reprimirlo, y la yerba sí, tomada en tiempo de calor
        con agua fría, como la usan los indios, y en tiempo frío o templado con agua caliente o templada…”
    </p>
</blockquote>
<p class="text-justify">
    Los ibéricos que veían a los indígenas tomarlo, llamaban al mate «hierba del demonio» por desconocer la práctica y
    efectos.nota 1​Sostenían además que era una bebida de haraganes, ya que los nativos dedicaban varias horas del día a
    esta práctica. Sin embargo, otros cronistas, como Acarete du Biscay consideraban que esa yerba era muy útil para los
    trabajadores en las minas de Perú, asegurando que "sin esa yerba (con la cual preparan una bebida refrescante, con
    agua y azúcar, que debe beberse tibia) los habitantes del Perú, salvajes y otros, especialmente los que trabajan en
    las
    minas, no podrían subsistir, porque el suelo está lleno de vetas minerales y los vapores que se desprenden los
    sofocarían y nada sino ese brebaje puede restaurarlos, ya que los hace revivir y los devuelve a su antiguo vigor".
    <br><br>

    Algunos cronistas españoles relatan en sus diarios que los nativos llevaban, junto a las armas, unas pequeñas bolsas
    de cuero (guayacas) en las que guardaban hojas de yerba mate triturada y tostada que masticaban o colocaban en una
    calabaza con agua y sorbían, usando sus dientes como filtro o por medio de un canuto de caña. Según ellos, lejos de
    aflojarlos, estas hojas les daban mayor resistencia para las largas marchas o para realizar las labores
    diarias.<br><br>

    El mate se originó como un rito de los avá en el territorio que hoy ocupa parte de Paraguay, el noreste argentino y
    el sur de Brasil. Ellos sepultaban los restos de sus seres queridos y en ese mismo lugar plantaban yerba mate, y
    luego
    de que la planta crecía, la cosechaban, preparaban la infusión y la tomaban «en rueda» con sus familias. Realizaban
    estos ritos porque creían que de esa manera el espíritu de sus seres allí enterrados crecería en la planta de yerba
    mate y
    pasaría a través del mate a su cuerpo para permanecer junto a ellos. También junto a la hierba, solían plantar
    diferentes vegetales ya que creían que esto favorecería el crecimiento de la planta.
</p>

<h3>Los mates más antiguos</h3>
<p class="text-justify">
    Para conocer los primeros momentos en la historia del mate como infusión de la yerba Ilex paraguariensis A.St.-Hil.
    1822, tenemos que remitirnos a la historia de la ciudad de Santa Fe. Los primeros utensilios y los más antiguos
    conocidos, se encuentran en esta ciudad y fueron excavados en el sitio arqueológico de Santa Fe la vieja. Mucho
    antes de que se usaran los utensilios que hoy conocemos, los indígenas bebían la infusión en forma de té,
    agregándole agua caliente o fría a la yerba mate. El recipiente utilizado era el bernegal, hecho de calabaza grande
    (Cucurbita moschata) cortada por el medio mientras con el labio superior impide que la yerba pase a la boca
    sorbiéndola entre los dientes: "ellos beben el agua entre los dientes delanteros como por un chupador" decía Florián
    Paucke. Los españoles luego, introdujeron el uso de una suerte de cuchara de metal llamada «apartador» con el cual
    detienen la yerba molida mientras sorben el té. Con el paso del tiempo, el bernegal de calabaza, sería reemplazado
    por vasijas de arcilla cocida con la misma forma de la media calabaza. En las excavaciones llevadas a cabo en el
    sitio arqueológico de Cayastá, se encontraron restos del bernegal de arcilla con decorados, tal como los elaborados
    en la Santa Fe virreinal. Estos utensilios pueden ser considerados los más antiguos antecesores conocidos de los
    mates que hoy utilizamos para la infusión de Ilex paraguariensis. Posteriormente entre ciertas clases sociales
    dentro del ambiente urbano, se introduce el uso del mate o calabaza pequeña (Lagenaria siceraria) utilizada junto a
    la bombilla, consistente en un "un cañito de madera o alguna caña" según nos revela Dobrizoffer.
</p>

<h3>La llegada del mate a Europa</h3>
<p class="text-justify">
    La historia de la yerba mate y su reafirmación en la cultura popular como alimento nativo, tuvo a la ciudad de Santa
    Fe como una de las protagonista principales. Esto es debido a que el 31 de diciembre de 1662, a solicitud de Santa
    Fe y de Asunción, se establece como puerto preciso a Santa Fe obligando a las embarcaciones del Paraguay que
    cumplieran su registro en Santa Fe, "siendo una de sus causas principales del pedido por la Asunción, el que como
    los marineros que conducían las embarcaciones eran todos naturales de aquella provincia, con la mayor distancia de
    su país o por inclinación novedosa de los ánimos, no dejaran su natural residencia desamparando a sus mujeres e
    hijos" . Así mediante cédula real, se obligaba a desembarcar en Santa Fe, toda mercadería proveniente del Paraguay
    para ser distribuida hacia el mundo, entren ellas, la Yerba Mate, destinada a Chile y el Alto Perú. Los españoles
    observaron que a los guaraníes de Paraguay, Argentina y Brasil el mate los fortificaba para el trabajo y en caso de
    necesidad les servía de alimento. Hacia 1714, su uso se había extendido primero al resto de Argentina y Tarija ya
    que compartían una cultura de y tradiciónes igual a la gaucha por no decir lo mismo, luego al Alto Perú Chuquisaca,
    Santa Cruz, Beni y Pando. Después extendieron hacia Chile. Los británicos de Chile (que se ocupaban de la trata de
    esclavos traídos de África) vieron que también beneficiaba a los negros, lo probaron y lo llevaron a Londres, donde
    fue muy bien recibido. Incluso se pensó en reemplazar el tradicional uso del té por esta bebida, ya que era más
    provechosa e incluso más barata; pero como las misiones jesuíticas del Paraguay eran su único productor, y el
    comercio del té les reportaba tan buenas ganancias, se desechó la idea.
</p>

<h3>Consumo</h3>
<p class="text-justify">
    La forma de preparación del mate fue transmitida por los guaraníes a los colonizadores españoles y portugueses (en
    portugués también se le llama chimarrão). Con el transcurso del tiempo fue adoptada como bebida tradicional de los
    pobladores de Paraguay, Uruguay, Argentina, centro y sur de Chile, sur de Brasil, y Bolivia, llegando a difundirse
    su consumo hasta Estados Unidos y Perú.<br><br>

    Sigue siendo una infusión de gran popularidad en casi todos los países mencionados, sobre todo en Paraguay,
    Argentina, Uruguay y sur de Chile y Brasil, teniendo la particularidad de que es bebido diariamente por gran parte
    de la población. En estos países el mate forma parte de la actividad cotidiana en muchos hogares y, en algunos
    casos, en oficinas, donde es muy común ver a profesionales trabajando frente a sus computadoras con un termo y
    «mate» acompañando su actividad. Es por sus características beneficiosas​ y por su bajo costo la bebida por
    antonomasia del estudiante universitario en estos países. El mate posee una vajilla especial: el recipiente
    principal (llamado «mate» o «porongo»), la «bombilla» (una especie de pajita de metal, madera etc. con filtro), y la
    «pava» o ―añadido desde el siglo xx― el termo. A este conjunto se suma frecuentemente un ensamble portátil (casi
    siempre constituido principalmente con metal) que reúne dos recipientes: la yerbera (es decir el recipiente en donde
    se deposita la yerba para cebar el mate) y la azucarera para el mate dulce.<br><br>

    Incluso la «pava» (o tetera) con la cual se suele calentar y verter el agua para el mate suele ser especial. Es muy
    semejante a una tetera común, aunque su pico tiene una muesca que facilita el vertido de un chorro fino del agua
    caliente dentro de los recipientes. Incluso existen bandejas especiales con molduras para acomodar la vajilla
    precitada y el plato en el cual se suelen colocar galletitas, tortitas fritas o «facturas» con las que
    tradicionalmente se acompaña el mate.
</p>

