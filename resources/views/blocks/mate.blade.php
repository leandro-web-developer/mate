<h3>Mate amargo</h3>
<p class="text-justify">
    En algunas partes del Cono Sur se prefiere beber mate amargo, especialmente en Paraguay, Uruguay, sur de Brasil,
    Argentina y Bolivia (departamento de Tarija y el Gran Chaco). Es la forma más habitual de tomar mate. También se lo
    conoce como cimarrón (chimarrão, en Brasil).
</p>

<h3>Mate dulce</h3>
<p class="text-justify">
    La diferencia con el mate amargo consiste en que en cada cebada se incorpora azúcar a gusto del bebedor. Esta forma
    de preparación es muy difundida pocas regiones de Bolivia en el Chaco de Santa Cruz, en Chuquisaca y en el
    departamento de Tarija, En Argentina, como en la provincia de Santiago del Estero, Córdoba (Argentina), Cuyo, Región
    metropolitana de Buenos Aires, entre otros. En Chile, esta forma de preparación del mate está difundida
    mayoritariamente en zonas rurales. La cucharadita de azúcar o miel debe caer al borde de la zanja que forma la
    bombilla en la yerba, no por todo el mate. Una variante es endulzar solo el primer mate para cortar el amargor del
    primero, suavizando así los posteriores.<br><br>

    En el dulce también suelen agregarse edulcorantes artificiales, tanto por problemas de salud como la diabetes, como
    por gusto. Como endulzante alternativo es preferible la natural ka'á he'é (Stevia rebaudiana), que es una de las
    hierbas cuyas hojas se agregan para dar un toque dulzón. Se usa principalmente en el Paraguay. En Perú está
    difundido en zonas rurales, y se prepara con coca o dulce estilo a té con rodajas pequeñas de limón o
    naranja.<br><br>

    La calabaza donde se toma mate cimarrón no se usa para consumir el dulce, ya que existe la tradición de que el sabor
    del azúcar perjudicaría su posterior utilización para cebar mate amargo, se dice que se estropea el sabor del mate.
</p>

<h3>Mate de leche</h3>
<p class="text-justify">
    La diferencia con el mate amargo es que en lugar de cebar con agua, se lo hace con leche y azúcar. Esta variante
    tiene la desventaja de no poder limpiar fácilmente la bombilla y el mate, por eso suelen utilizarse unos distintos a
    los de las variantes tradicionales. En el Paraguay, también se acostumbra a cebar lo que allí se denomina «mate
    dulce», que se prepara con leche caliente endulzada (o con azúcar dorada), cambiando la yerba por coco rallado, o
    poniendo primeramente yerba y sobre ella coco rallado.
</p>

<h3>Mate con otras hierbas</h3>
<p class="text-justify">
    Es posible adicionar otras hierbas («yuyos») a la infusión (tanto al agua con que se ceba el mate como directamente
    a la yerba mate) para darle un sabor diferente o con fines medicinales. Es común el agregado de hierbas con
    propiedades digestivas o sedativas, por ejemplo: coca, peperina, poleo, melisa, toronjil, menta, cola de caballo,
    congorosa (que disminuye la acidez PH del mate), incayuyo, té de burro, ajenjo, carqueja, anís, etc.<br><br>

    En el Paraguay esta bebida se consume casi en un 90 % con hierbas o yuyos medicinales nativos de la región, que los
    indígenas guaraníes ya conocían y utilizaban. La variedad de hierbas adicionadas es amplia, comprendiendo anís,
    ajenjo, sen, semillas de lino, boldo, manzanilla, jaguareté ka'a, hojas de eucalipto, hojas de menta, suico, siempre
    vive, azafrán, caña brava, pynó gusú rapó, doradilla, urusu he’ê, borraja, limón sutíl, malva, yate’i ka’a y otras.
</p>

<h3>Mate de té</h3>
<p class="text-justify">
    El mate de té, es considerado otra variante del mate dulce; no se realiza con yerba mate y es muy común en la
    provincia de Entre Ríos y Uruguay y muy popular entre los chicos y adolescentes. Como su nombre lo indica, se
    prepara con té (negro generalmente) y limón como ingredientes principales; no se usa la calabaza del mate, sino
    algún recipiente similar a una taza (ya que existe una tradición que dice que el mate dulce y con té estropearía el
    sabor de la calabaza), lo que tienen en común es que sí se utiliza bombilla. Opcionalmente se pueden agregar una
    gran variedad de ingredientes, siendo populares algunos «yuyos» o hierbas como, por ejemplo, menta, cedrón, boldo,
    tilo, manzanilla. También es popular agregar algunos otros aditivos como cáscaras de naranja o rodajas de esta misma
    fruta, trozos de manzana, coco rallado (Influencia paraguaya), o incluso hay quienes gustan de agregarle cáscara de
    banana (Influencia brasilera), entre otros. Una vez seleccionados los ingredientes y colocados en la taza se agrega
    el agua caliente acompañando cada cebada de azúcar (o algún edulcorante si se prefiere). Es tradición para este tipo
    de mate el tomarlo en las «siestas» durante el invierno, debido a que el agua es un poco más caliente que el mate
    tradicional (entre 80 y 90 °C) y debido también a las propiedades digestivas del té con limón y de los «yuyos»
    normalmente utilizados. El mismo no guarda ninguna relación con la yerba mate. Lo que sí tiene en común es el
    compartirlo, el agua caliente y la bombilla.
</p>

<h3>Tereré</h3>
<p class="text-justify">
    El tereré (palabra de origen guaraní)82​ es una bebida tradicional, de amplio consumo en Paraguay y algunas
    provincias argentinas (las del nordeste y Entre Ríos).<br><br>

    Una forma de tereré, consistente en una mezcla de agua bien fría con yerba mate, remedios refrescantes naturales
    (hierbas medicinales) y hielo. Como hierbas suelen emplearse la menta (Mentha arvensis), la menta peperina, el
    cedrón (Aloysia triphylla), burrito, el kokú (Allophylus edulis), la cola de caballo o las cáscaras de limón.<br><br>

    La otra forma de tereré consiste en poner en un recipiente de metal tres dedos de altura de yerba mate y hielo.
    Preparar en una jarra o termo jugo de diversos sabores naranja, lima, limón con abundante hielo y cebarlo.<br><br>

    La yerba mate es puesta en maceración en agua fría y proporciona una bebida agradable por su efecto refrescante,
    especialmente en jornadas con altas temperaturas, evitando así los inconvenientes del mate hervido (cocido). Esta
    modalidad es también utilizada en la región pampeana de Argentina.
</p>

